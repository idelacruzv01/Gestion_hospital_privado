    <h1>Política de Privacidad y Confidencialidad</h1>

    <p>Esta aplicación web está destinada exclusivamente al uso interno del personal autorizado del <strong>Hospital Privado</strong>. Su finalidad es facilitar la gestión administrativa y el acceso a información de contacto de profesionales y entidades colaboradoras.</p>

    <h2>1. Responsable del tratamiento</h2>
    <p><strong>Hospital Privado</strong> es el responsable del tratamiento de los datos personales a los que se accede a través de esta aplicación.</p>

    <h2>2. Finalidad del tratamiento</h2>
    <p>Los datos personales que se muestran tienen carácter profesional y se utilizan únicamente para fines administrativos internos y de contacto entre departamentos o entidades colaboradoras.</p>

    <h2>3. Base jurídica</h2>
    <p>El tratamiento se basa en el interés legítimo del hospital y en la ejecución de funciones propias del personal autorizado.</p>

    <h2>4. Confidencialidad y seguridad</h2>
    <p>El usuario se compromete a tratar la información accesible con la máxima confidencialidad y a no divulgar, copiar ni transferir datos a terceros sin autorización expresa del hospital. Cualquier uso indebido podrá implicar responsabilidades disciplinarias o legales.</p>

    <h2>5. Derechos de las personas</h2>
    <p>Las personas cuyos datos aparecen en la aplicación pueden ejercer sus derechos de acceso, rectificación, supresión, limitación y oposición, dirigiendo una solicitud al responsable del tratamiento a través del correo electrónico: <a href="mailto:protecciondatos@hospitalprivado.es">protecciondatos@hospitalprivado.es</a>.</p>

    <h2>6. Conservación de la información</h2>
    <p>Los datos se conservarán únicamente durante el tiempo necesario para la finalidad descrita o mientras se mantenga la relación laboral o contractual con el hospital.</p>

    <h2>7. Seguridad de la sesión</h2>
    <p>El acceso está restringido mediante credenciales personales. Las sesiones de usuario caducan automáticamente tras un periodo de inactividad para proteger la información frente a accesos no autorizados.</p>

    <h2>8. Aceptación de la política</h2>
    <p>Al acceder a la aplicación, el usuario reconoce haber leído y aceptado esta Política de Privacidad y Confidencialidad, y se compromete a cumplir las normas de uso responsable establecidas por el hospital.</p>

